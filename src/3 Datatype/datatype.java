public class datatype{
    public static void main(String [] args){
        byte a=10;          // 1 byte
        short b=200;        // 2 byte
        int c =12;          // 4 byte
        long l=10000L;      // 8 byte
        float f=1.2f;       // 4 byte
        double d=12.234d;   // 8 byte
        char g = 'A';       // 2 byte
        boolean o= true;    // 1 bit
        // ALL Are the  Primative Datatype 

        System.out.println("1 byte :" + a);
        System.out.println("2 Short :" + b);
        System.out.println("3 int :" + c);
        System.out.println("4 long :" + l);
        System.out.println("5 float :" + f);
        System.out.println("6 double :" + d);
        System.out.println("7 char :"+ g);
        System.out.println("8 boolean :" + o);
        //+ is used for string concatenation (joining values)
    }
}
